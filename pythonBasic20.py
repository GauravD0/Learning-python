# + - Addition
# - - Subtraction
# * - Multiplication
# / - Division
# % - Modulus
# ** - Exponentiation
# // - Floor Division
print(10 + 5)   # Addition
print(10-5)   # Subtraction
print(10 * 5)   # Multiplication
print(10 / 5)   # Division
print(10 % 5)   # Modulus
print(10 ** 5)  # Exponentiation
print(10 // 5)  # Floor Division
