#There are certain methods in string like
#To convert the string to upper case we use upper() function
x = "Gaurav"
print(x.upper()) #it will print the string upper case

#to convert to lower case we use lower() function
print(x.lower()) #it will print the string lower case

#to print to the capitalize first letter 
print(x.capitalize()) #it will print the string from tunner

#to find the indexing of the substring
print(x.index("Gaur"))
#it will print the starting index of the substring which is 0

